    boolean testData(){
       if(txtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Fall");
            return false;
       }
       else { //đã có dữ liệu check: id là số? ,id>=0
            try {//là số
                 if(Integer.parseInt(txtID.getText())<0);
            } catch (Exception e) { // khong la so
                JOptionPane.showMessageDialog(this," id khong phai so");
                return false;
            }
           // check name
           if(txtNAME.getText().isEmpty()){
                JOptionPane.showMessageDialog(this,"name trong");
                return false;
           }else{ //checktenchu cai :k ap dungtieng viet
            if(!txtNAME.getText().matches("[a-zA-Z ]+")){ // nếu có lỗi sẽ báo sai định dạng
                JOptionPane.showMessageDialog(this,"name sai dinh dang");
                return false;
            }
           }
           return true;
       }
    }
     
