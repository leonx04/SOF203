    private void btnUPDATEActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUPDATEActionPerformed
        // TODO add your handling code here:
        index = Table.getSelectedRow();
        int ID = Integer.parseInt(
            Table.getValueAt(index, 0).toString()
        );
        // lấy đối tượng mới tuỏm
        SinhVien sv = this.readForm();
        if(service.updateSinhVien(ID, sv)> 0){
            JOptionPane.showMessageDialog(this, " thanh cong update");
            this.fillTable(service.getAll());
        }
        else {
            JOptionPane.showMessageDialog(this, "UPDATE THAT BAI");
        }
    }//GEN-LAST:event_btnUPDATEActionPerformed

