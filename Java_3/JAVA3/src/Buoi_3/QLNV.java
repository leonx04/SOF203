    private void btnOkActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOkActionPerformed
        // TODO add your handling code here:
        if (check()) {
        String ho = txtHo.getText();
        String ten = txtTen.getText();
        String biDanh = txtBiDanh.getText();
        String pass = new String(txtPass.getPassword());
        String gioiTinh = "";
        if (btnNam.isSelected()) {
            gioiTinh = "Nam";
        } else if (btnNu.isSelected()) {
            gioiTinh = "Nữ";
        } else if (btnError.isSelected()) {
            gioiTinh = "Không xác định";
        }
        String queQuan = Combobox.getSelectedItem().toString();
        String soThich = "";
        if (check1.isSelected()) {
            soThich += "Ăn chơi ";
        }
        if (check2.isSelected()) {
            soThich += "Nhảy nhót ";
        }
        if (check3.isSelected()) {
            soThich += "Ngủ nghỉ";
        }
        String ghiChu = txtArea.getText();

        String thongTin = "Họ: " + ho + "\n" +
                          "Tên: " + ten + "\n" +
                          "Bí danh: " + biDanh + "\n" +
                          "Password: " + pass + "\n" +
                          "Giới tính: " + gioiTinh + "\n" +
                          "Quê quán: " +"-"+ queQuan + "\n" +
                          "Sở thích: " + soThich + "\n" +
                          "Ghi chú: " + ghiChu;

        JOptionPane.showMessageDialog(this, thongTin);
    }
    }//GEN-LAST:event_btnOkActionPerformed

