    public boolean  check(){
        if(txtHo.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Họ rỗng");
            return false;
        }else{
            if(!txtHo.getText().matches("[a-zA-Z ]+")){
                JOptionPane.showMessageDialog(this, "Sai dinh dang");
                return false;
            }
        }
        if(txtTen.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Tên  rỗng");
            return false;
        }else{
            if(!txtTen.getText().matches("[a-zA-Z ]+")){
                JOptionPane.showMessageDialog(this, "Sai dinh dang");
                return false;
            }
        }
        if(txtPass.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Pass rỗng");
            return false;
        }else{
            if(!txtPass.getText().matches("[a-zA-Z ]+")){
                JOptionPane.showMessageDialog(this, "Sai dinh dang");
                return false;
            }
        }
        if(txtBiDanh.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Bí danh rỗng");
            return false;
        }else{
            if(!txtBiDanh.getText().matches("[a-zA-Z ]+")){
                JOptionPane.showMessageDialog(this, "Sai dinh dang");
                return false;
            }
        }
        if(txtArea.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Ghi chu rỗng");
            return false;
        }else{
            if(!txtArea.getText().matches("[a-zA-Z ]+")){
                JOptionPane.showMessageDialog(this, "Sai dinh dang");
                return false;
            }
        }
        JOptionPane.showMessageDialog(this, "Đã đúng định  dạng");
        return true;
        
    }
    
