    void fillTable(List<NhanVien> list){
        model=(DefaultTableModel) Table.getModel();
        model.setRowCount(0);
        for(NhanVien nv : list){
            model.addRow(nv.toData());
        }
    }
    
    void showData(int index){
        NhanVien nv = service.getAll().get(index);
        txtMa.setText(nv.getMa());
        txtMK.setText(nv.getMatkhau());
        txtTen.setText(nv.getTen());
        if(nv.getGioiTinh().equals("Nam")){
            rdoNam.setSelected(true);
        }else{
            rdoNu.setSelected(true);
        }
        txtNgaySinh.setText(nv.getNgaySinh());
        txtDiaChi.setText(nv.getDiaChi());
        cboTrangThai.setSelectedIndex(nv.getTrangThai());
    }
    NhanVien readForm(){
        NhanVien nv = new NhanVien();
        String Ma, MatKhau, Ten,GioiTinh, NgaySinh,DiaChi;
        int TrangThai;
        
        Ma= txtMa.getText();
        MatKhau= txtMK.getText();
        Ten = txtTen.getText();
        if(rdoNam.isSelected()){
            GioiTinh=("Nam");
        }else{
            GioiTinh=("Nữ");
        }
        NgaySinh = txtNgaySinh.getText();
        DiaChi = txtDiaChi.getText();
        TrangThai = cboTrangThai.getSelectedIndex();
        return new NhanVien(Ma, MatKhau, Ten, GioiTinh, NgaySinh, DiaChi, TrangThai);
    }
    
    boolean check(){
    if(txtMa.getText().isEmpty()){
        JOptionPane.showMessageDialog(this, "Ma trong");
        return false;
    }
    return true;
    }
