    private void btnADDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnADDActionPerformed
        // TODO add your handling code here:
        if(check()){
            NhanVien nv =this.readForm();
            if(service.checkTrung(nv.getMa())!=null){
                JOptionPane.showMessageDialog(this, "ma trung khong them duoc");
            }else{
                if(service.addNhanVien(nv)>0){
                    JOptionPane.showMessageDialog(this, "them thanh cong");
                    this.fillTable(service.getAll());
                }else{
                    JOptionPane.showMessageDialog(this, "them that bai");
                }
            }
        }
    }//GEN-LAST:event_btnADDActionPerformed
    
    
    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if(check()){
            NhanVien nv= this.readForm();
            index= Table.getSelectedRow();
            String ma = Table.getValueAt(index, 0).toString();
            if(service.updateNV(nv, ma)>0){
                JOptionPane.showMessageDialog(this, "update thanh cong");
                this.fillTable(service.getAll());
            }else{
                JOptionPane.showMessageDialog(this, "update that bai");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        index = Table.getSelectedRow();
        String Ma = Table.getValueAt(index, 0).toString();
        if(service.deleteNV(Ma)>0){
                JOptionPane.showMessageDialog(this, "Xoa thanh cong");
                this.fillTable(service.getAll());
        }else{
            JOptionPane.showMessageDialog(this, "Xoa that bai");
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    
