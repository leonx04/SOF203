    void fillTable(List<XeMay> list) {
        mol = (DefaultTableModel) tbl_Xe.getModel();
        mol.setRowCount(0);
        for (XeMay x : list) {
            mol.addRow(x.toData());
        }
    }

    void showData(int index) {
        XeMay x = ser.getAll().get(index);
        txt_ma.setText(x.getMa());
        txt_ten.setText(x.getTen());
        txt_ban.setText(String.valueOf(x.getBan()));
        txt_nhap.setText(String.valueOf(x.getNhap()));
        cbo_Soluong.setSelectedIndex(x.getSoLuong());
        cbo_trangthao.setSelectedIndex(x.getTrangThai());
    }

    XeMay readForm() {
        String ma, ten;
        double nhap, ban;
        int soLuong, trangThai;

        ma = txt_ma.getText();
        ten = txt_ten.getText();
        nhap = Double.parseDouble(txt_nhap.getText());
        ban = Double.parseDouble(txt_ban.getText());
        soLuong = cbo_Soluong.getSelectedIndex();
        trangThai = cbo_trangthao.getSelectedIndex();

        return new XeMay(ma, ten, nhap, ban, soLuong, trangThai);
    }

    boolean check() {
        if (txt_ma.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ma rong");
            return false;

        }
        if (txt_ten.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "ten rong");
            return false;
        }
        if (txt_ban.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "gia ban rong");
            return false;
        } else {
            try {
                if (Double.parseDouble(txt_ban.getText()) < 0) {
                    JOptionPane.showMessageDialog(this, "gia lon hon 0");
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "gia phai la so");
                return false;
            }
        }
        if (txt_nhap.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "gia nhap rong");
            return false;
        } else {
            try {
                if (Double.parseDouble(txt_nhap.getText()) < 0) {
                    JOptionPane.showMessageDialog(this, "gia nhap lon hon 0");
                    return false;
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "gia phai la so");
                return false;
            }
        }
        return true;
    }

