    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        if (check()) {
            XeMay x = this.readForm();
            if (ser.checkTrung(x.getMa()) != null) {
                JOptionPane.showMessageDialog(this, "ma trung");

            } else {
                if (ser.addXe(x) > 0) {
                    JOptionPane.showMessageDialog(this, "them thanh cong");
                    this.fillTable(ser.getAll());
                } else {
                    JOptionPane.showMessageDialog(this, "them that bai");
                }
            }
        }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (check()) {
            index = tbl_Xe.getSelectedRow();
            String ma = tbl_Xe.getValueAt(index, 0).toString();
            if (ser.deleteXe(ma) > 0) {
                JOptionPane.showMessageDialog(this, "xoa thanh cong");
                this.fillTable(ser.getAll());
            } else {
                JOptionPane.showMessageDialog(this, "xoa that bai");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        XeMay x = this.readForm();
        index = tbl_Xe.getSelectedRow();
        String ma = tbl_Xe.getValueAt(index, 0).toString();
        if (ser.updateXe(x, ma) > 0) {
            JOptionPane.showMessageDialog(this, "sua thanh cong");
            this.fillTable(ser.getAll());
        } else {
            JOptionPane.showMessageDialog(this, "sua that bai");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

